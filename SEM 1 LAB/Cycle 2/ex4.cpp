//4.Program to find the size of fundamental data type size of the data type int float char double

#include <iostream>
using namespace std;

int main()
{
  cout << sizeof(int) << endl;
  cout << sizeof(float) << endl;
  cout << sizeof(char) << endl;
  cout << sizeof(double) << endl;

  return 0;
}

//output: 4 4 1 8